    public function __construct()
    {
        $this->transliterator = Transliterator::create('Any-Latin; Latin-ASCII; [\u0100-\u7fff] remove');
        $this->englishInflector = new EnglishInflector();
    }

    public static function lang(): string
    {
        return 'en';
    }

    public function langName(): string
    {
        return 'English (beta)';
    }

    public function normalize(string $word): string
    {
        $transliterated = $this->transliterator->transliterate($word);
        if (false === $transliterated) {
            return $word;
        }

        return trim(mb_strtolower($transliterated));
    }

    /**
     * We handle singular and plural versions.
     *
     * @return string[]
     */
    public function variations(string $normalized): array
    {
        return array_merge(
            [$normalized],
            $this->englishInflector->singularize($normalized),
            $this->englishInflector->pluralize($normalized)
        );
    }

